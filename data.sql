CREATE TABLE transaccion (
	id INT PRIMARY KEY AUTO_INCREMENT,
	total INT,
	created DATETIME,
	estado VARCHAR(255)
);

CREATE TABLE proveedor(
	id INT PRIMARY KEY AUTO_INCREMENT,
	nombre VARCHAR(255)
);

CREATE TABLE actividad (
	id INT PRIMARY KEY AUTO_INCREMENT,
	proveedor_id INT,
	nombre VARCHAR(255),
	FOREIGN KEY (proveedor_id) REFERENCES proveedor(id)
);

CREATE TABLE actividad_evento (
	id INT PRIMARY KEY AUTO_INCREMENT,
	actividad_id INT,
	fecha_inicio DATETIME,
	fecha_termino DATETIME,
	FOREIGN KEY (actividad_id) REFERENCES actividad(id) 
);

CREATE TABLE item (
	id INT PRIMARY KEY AUTO_INCREMENT,
	evento_id INT,
	trasaccion_id INT,
	cantidad INT,
	created DATETIME,
	updated DATETIME,
	FOREIGN KEY (evento_id) REFERENCES actividad_evento(id),
	FOREIGN KEY (trasaccion_id) REFERENCES transaccion(id)
);

CREATE TABLE paquete(
	id INT PRIMARY KEY AUTO_INCREMENT,
	item_id INT,
	estado VARCHAR(255),
	created DATETIME,
	updated DATETIME,
	FOREIGN KEY (item_id) REFERENCES item(id)
);

CREATE TABLE entrada(
	id INT PRIMARY KEY AUTO_INCREMENT,
	codigo VARCHAR(255),
	fecha_acceso DATETIME,
	created DATETIME,
	updated DATETIME,
	item_id INT,
	estado VARCHAR(255)
);

